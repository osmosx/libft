#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*element;

	if (*lst == 0)
	{
		*lst = new;
		return ;
	}
	else
	{
		element = new;
		element->next = *lst;
		*lst = element;
	}
}
