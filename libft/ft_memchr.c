#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*t;
	unsigned char	src;

	src = c;
	i = 0;
	t = (unsigned char *)s;
	while (i < n)
	{
		if (t[i] == src)
			return (t + i);
		i++;
	}
	return (0);
}
